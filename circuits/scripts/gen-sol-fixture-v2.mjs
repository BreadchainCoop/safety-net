// Writes the real v2 proof as a Solidity fixture for the Foundry integration test.
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const proof = JSON.parse(readFileSync(join(root, "build/proof_v2.json"), "utf8"));
const pub = JSON.parse(readFileSync(join(root, "build/public_v2.json"), "utf8"));
const meta = JSON.parse(readFileSync(join(root, "build/input_v2.meta.json"), "utf8"));
if (pub.length !== 6) throw new Error(`expected 6 public signals, got ${pub.length}`);
const [ax, ay] = proof.pi_a, [[bx1, bx2], [by1, by2]] = proof.pi_b, [cx, cy] = proof.pi_c;
const sol = `// SPDX-License-Identifier: UNLICENSED
pragma solidity ^0.8.28;

/// @dev GENERATED by circuits/scripts/gen-sol-fixture-v2.mjs — do not edit by hand.
///      A real FluClaimV2 proof over two TEST-key emails, bound to ${meta.claimant}.
library FluClaimV2Fixture {
  string internal constant PROVIDER_DOMAIN = '${meta.providerDomain}';
  string internal constant BINDING_DOMAIN = '${meta.bindingDomain}';
  address internal constant CLAIMANT = ${meta.claimant};
  bytes32 internal constant PUBKEY_HASH_A = ${meta.pubkeyHashA};
  bytes32 internal constant PUBKEY_HASH_B = ${meta.pubkeyHashB};

  function a() internal pure returns (uint256[2] memory) { return [uint256(${ax}), uint256(${ay})]; }
  function b() internal pure returns (uint256[2][2] memory) {
    return [[uint256(${bx2}), uint256(${bx1})], [uint256(${by2}), uint256(${by1})]];
  }
  function c() internal pure returns (uint256[2] memory) { return [uint256(${cx}), uint256(${cy})]; }
  function signals() internal pure returns (uint256[6] memory) {
    return [${pub.map((s) => `uint256(${s})`).join(", ")}];
  }
}
`;
writeFileSync(join(root, "../test/fixtures/FluClaimV2Fixture.sol"), sol);

// Also emit the portable proof bundle the web UI + encoding test consume.
const bundle = { providerDomain: meta.providerDomain, bindingDomain: meta.bindingDomain, proof, publicSignals: pub };
writeFileSync(join(root, "build/flu-bundle-v2.json"), `${JSON.stringify(bundle, null, 2)}\n`);

console.log("wrote test/fixtures/FluClaimV2Fixture.sol + build/flu-bundle-v2.json");
console.log("pubkeyHashA(sig[0])=" + pub[0] + " expected " + BigInt(meta.pubkeyHashA));
console.log("pubkeyHashB(sig[1])=" + pub[1] + " expected " + BigInt(meta.pubkeyHashB));
