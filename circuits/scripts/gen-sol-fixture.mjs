// Writes the real proof + public signals as a Solidity fixture library consumed by
// test/unit/FluClaimProofFixtureUnit.sol in the main repo — the on-chain validation
// that the deployed signal layout, packing, and commitment math match the circuit.
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const proof = JSON.parse(readFileSync(join(root, "build/proof.json"), "utf8"));
const pub = JSON.parse(readFileSync(join(root, "build/public.json"), "utf8"));
const meta = JSON.parse(readFileSync(join(root, "build/input.meta.json"), "utf8"));

if (pub.length !== 7) throw new Error(`expected 7 public signals, got ${pub.length}`);

// snarkjs pi_b limbs are emitted in reverse coordinate order for the EVM pairing
const [ax, ay] = proof.pi_a;
const [[bx1, bx2], [by1, by2]] = proof.pi_b;
const [cx, cy] = proof.pi_c;

const sol = `// SPDX-License-Identifier: UNLICENSED
pragma solidity ^0.8.28;

/// @dev GENERATED by circuits/scripts/gen-sol-fixture.mjs — do not edit by hand.
///      A real Groth16 proof over circuits/fixtures/flu-result.eml (DKIM-signed with the
///      committed TEST key for ${meta.domain}), bound to claimant ${meta.claimantAddress}.
library FluClaimProofFixture {
  string internal constant DOMAIN = '${meta.domain}';
  address internal constant CLAIMANT = ${meta.claimantAddress};
  bytes32 internal constant EMAIL_COMMITMENT = ${meta.emailCommitment};

  function a() internal pure returns (uint256[2] memory) {
    return [uint256(${ax}), uint256(${ay})];
  }

  function b() internal pure returns (uint256[2][2] memory) {
    return [[uint256(${bx2}), uint256(${bx1})], [uint256(${by2}), uint256(${by1})]];
  }

  function c() internal pure returns (uint256[2] memory) {
    return [uint256(${cx}), uint256(${cy})];
  }

  function signals() internal pure returns (uint256[7] memory) {
    return [${pub.map((s) => `uint256(${s})`).join(", ")}];
  }
}
`;

const out = join(root, "../test/fixtures/FluClaimProofFixture.sol");
writeFileSync(out, sol);
console.log(`wrote ${out}`);
console.log(`pubkeyHash=${pub[0]}`);
console.log(`toAddressHash(hex)=0x${BigInt(pub[3]).toString(16).padStart(64, "0")}`);
console.log(`commitment       =${meta.emailCommitment}`);
